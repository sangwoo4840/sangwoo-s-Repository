package com.jangin.wow2d.wardrobe;

public class AuthVO {
	private String wr_cd;
	private String type;
	private String name;
	private String status;
	private String auth;

	public String getWr_cd() {
		return wr_cd;
	}

	public void setWr_cd(String wr_cd) {
		this.wr_cd = wr_cd;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getAuth() {
		return auth;
	}

	public void setAuth(String auth) {
		this.auth = auth;
	}
}
