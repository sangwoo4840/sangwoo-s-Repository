package com.jangin.wow2d.slide;

public class SlideVO {
	private String caption;
	private int width;
	private int height;
	private String kind;
	private String bodyLR;
	private String pillarLR;
	private String item_cd;
	private int qty;

	public String getCaption() {
		return caption;
	}

	public void setCaption(String caption) {
		this.caption = caption;
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

	public String getKind() {
		return kind;
	}

	public void setKind(String kind) {
		this.kind = kind;
	}

	public String getBodyLR() {
		return bodyLR;
	}

	public void setBodyLR(String bodyLR) {
		this.bodyLR = bodyLR;
	}

	public String getPillarLR() {
		return pillarLR;
	}

	public void setPillarLR(String pillarLR) {
		this.pillarLR = pillarLR;
	}

	public String getItem_cd() {
		return item_cd;
	}

	public void setItem_cd(String item_cd) {
		this.item_cd = item_cd;
	}

	public int getQty() {
		return qty;
	}

	public void setQty(int qty) {
		this.qty = qty;
	}
}
