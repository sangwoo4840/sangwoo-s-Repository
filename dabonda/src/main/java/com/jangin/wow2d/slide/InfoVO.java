package com.jangin.wow2d.slide;

public class InfoVO {
	private String body;
	private String jabara;
	private String pillar;

	public String getBody() {
		return body;
	}

	public void setBody(String body) {
		this.body = body;
	}

	public String getJabara() {
		return jabara;
	}

	public void setJabara(String jabara) {
		this.jabara = jabara;
	}

	public String getPillar() {
		return pillar;
	}

	public void setPillar(String pillar) {
		this.pillar = pillar;
	}
}
