package com.jangin.util.maps;

public class ApartmentVO {
	private int seq;
	private String yyyymm;
	private String apt_nm;
	private String household_no;
	private String area;
	private String location;
	private String addr;
	private String x;
	private String y;
	private String url;
	private String user;
	
	public int getSeq() {
		return seq;
	}

	public void setSeq(int seq) {
		this.seq = seq;
	}

	public String getYyyymm() {
		return yyyymm;
	}

	public void setYyyymm(String yyyymm) {
		this.yyyymm = yyyymm;
	}

	public String getApt_nm() {
		return apt_nm;
	}

	public void setApt_nm(String apt_nm) {
		this.apt_nm = apt_nm;
	}

	public String getHousehold_no() {
		return household_no;
	}

	public void setHousehold_no(String household_no) {
		this.household_no = household_no;
	}

	public String getLocation() {
		return location;
	}

	public void setLocation(String location) {
		this.location = location;
	}
	
	public String getArea() {
		return area;
	}

	public void setArea(String area) {
		this.area = area;
	}

	public String getAddr() {
		return addr;
	}

	public void setAddr(String addr) {
		this.addr = addr;
	}

	public String getX() {
		return x;
	}

	public void setX(String x) {
		this.x = x;
	}

	public String getY() {
		return y;
	}

	public void setY(String y) {
		this.y = y;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getUser() {
		return user;
	}

	public void setUser(String user) {
		this.user = user;
	}
}
